    <div class="text">
        <p>Personvernerklæring for westflyttebyra.no (West Flyttebyrå AS)
        West Flyttebyrå AS beskytter ditt personvern, og du kan være trygg når du besøker vår nettside.</p>
        <br><br>
        <p class="we-b">Automatisk innhentet informasjon</p>
        <p>Noe informasjon samles inn automatisk av West Flyttebyrå AS. Din IP-adresse blir registrert for å hjelpe med å diagnostisere problemer med webserveren og for å administrere nettsiden vår. Denne informasjonen brukes primært til generell trafikksporing og sikkerhet. Informasjonen som samles inn er ikke personlig identifiserbar.
        </p>
        <br><br>
        <p class="we-b">HVA ER PERSONOPPLYSNINGER?</p>
        <p>Personopplysninger er alle former for informasjon som kan identifisere en person, for eksempel navn, e-postadresse, telefonnummer og IP-adresse.
        </p>
        <br><br>
        <p class="we-b">HVORFOR SAMLER VI INN PERSONOPPLYSNINGER?</p>
        <p>Vi samler inn personopplysninger om deg for å kunne kontakte deg og gi deg tilbud gjennom annonser og nyhetsbrev.
        Ved å melde deg på vårt nyhetsbrev samtykker du til å motta dette jevnlig. Vi lagrer kun e-postadressen din, og den vil ikke bli brukt til andre formål enn å sende deg nyhetsbrev.
        </p>
        <br><br>
        <p class="we-b">HVORDAN SAMLER VI INN PERSONOPPLYSNINGER?</p>
        <p>De opplysningene vi samler inn, skjer via kontaktskjemaet vårt, påmelding til nyhetsbrevet og via våre kampanjesider. Vi samler inn informasjon som navn, e-postadresse og telefonnummer.
        </p>
        <br><br>
        <p class="we-b">HVEM HAR TILGANG TIL PERSONOPPLYSNINGENE?</p>
        <p>Vi er ansvarlige for behandlingen av dine personopplysninger og for å sikre at de ikke misbrukes eller kommer på avveie. Vi selger aldri dine personopplysninger videre til tredjeparter.
        </p>
        <br><br>
        <p class="we-b">HVOR LENGE LAGRER VI DATA?</p>
        <p>Vi oppbevarer ikke dine personopplysninger lenger enn nødvendig for å oppfylle det formålet de ble innhentet for. Dette betyr at vi kan beholde dine personopplysninger i en lengre periode etter din siste interaksjon med oss, men opplysningene blir slettet når de ikke lenger er nødvendige.
        </p>
        <br><br>
        <p class="we-b">DIN RETT TIL INNSYN, RETTING OG SLETTING</p>
        <p>Du har rett til innsyn i de opplysningene vi har lagret om deg, hva de brukes til og hvor lenge de blir lagret. Du har også rett til å be om endring av dine opplysninger hvis de vi har lagret er ukorrekte.
        Du har også rett til å kreve sletting av personopplysninger du mener er unødvendige. Du har også rett til å trekke tilbake ditt samtykke hvis du ønsker det.
        <br><br>
        Du har også rett til å klage til en tilsynsmyndighet.
        <br><br>
        Datatilsynet <u><a href="https://www.datatilsynet.no/" target="_blank">www.datatilsynet.no</a></u>
        </p>
        <br><br>
        <p class="we-b">BRUK AV INFORMASJONSKAPSLER (COOKIES)</p>
        <p>West Flyttebyrå AS bruker informasjonskapsler (cookies) for å spore informasjon om besøkende på nettstedet vårt. Informasjonen inkluderer dato og tidspunkt for besøket samt hvilke sider som ble besøkt. Denne informasjonen er ikke personlig identifiserbar. Informasjonskapslene vil forbedre brukeropplevelsen din.
        <br><br>
        Du kan velge å deaktivere informasjonskapsler på denne og andre nettsteder. Her kan du se hvordan du gjør det i nettleseren din.
